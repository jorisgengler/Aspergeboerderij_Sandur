<p><i>Bereiding: op vele manieren</i></p>
<p>Asperges kunt u op vele manieren bereiden. Traditioneel met ei, ham en krielaardappeltjes. Maar ook met vis, als heilbot of zalm, zijn heerlijke recepten bekend. Probeert u ook eens een asperge salade. En speciaal voor bij de borrel hebben we mini asperges. Deze hoeft u niet te schillen. Even koken (ca. 10 minuten), in een plakje ham rollen en smullen maar. </p>
