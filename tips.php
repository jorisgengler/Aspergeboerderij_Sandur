			<p><i>Schillen: van top tot teen </i></p>
<p>Schil de asperges bij voorkeur met een dunschiller. Begin met het schillen net onder de kop. De kop zelf niet schillen! De asperge wordt naar onderen toe dikker. Kijk daarom goed of onderaan ook alle schil er af komt. Wanneer dit niet goed gebeurt, vindt u ze later bij het eten als kleine sliertjes terug.</p>
