        	<h1>Informatie</h1>


			<p><i>Informatie over de asperge</i></p>
<p>In het plantenrijk behoort de asperge, de "Koningin onder de groenten", tot de familie van de lelieachtigen. Het is een struikachtige plant, die bij een juiste behandeling vanuit haar wortelstokken nieuwe scheuten vormt. Deze scheuten kennen wij als asperges. De officiële wetenschappelijke naam is Asparagus Officianalis. Officianalis betekent: geneeskrachtig.
Een exclusieve groente met een bijzondere smaak, die met veel vakmanschap op een nog puur ambachtelijke wijze wordt geteeld en geoogst. De witte asperges groeit in bedden op losse en goed doorlatende zandgronden. Slank en roomblank brengen zij menige liefhebber tot grote verrukking.</p><br />


<p><i>Oogsten</i></p>
<p>Het oogsten van witte asperges is puur handwerk. We oogsten de asperges met een speciaal steekmes. De geoogste asperges wordt in een aluminium bakje verzameld. Daarna gekoeld in fris water. De asperges worden gewassen en gesorteerd met een afsnij- en sorteerband. De asperges worden afgesneden op een lengte van 22 cm. Dit is nodig omdat de zogenaamde ondereinden te taai zijn voor consumptie. Na het oogstseizoen kan de plant weer uitgroeien tot een struik. Door assimilatie wordt dan voldoende energie verzameld om in het volgende oogstseizoen weer heerlijke aspergestengels te laten groeien.</p><br />


<p><i>Soorten </i></p>
<p>De asperges worden op kwaliteit, kleur en dikte gesorteerd. De mooiste asperges komen in klasse I. Minder mooie in klasse II. Binnen deze klassen worden de asperges nog weer onderverdeeld in wit en violet. De violette hebben eventjes de zon gezien voordat ze zijn gestoken en krijgen daardoor een klein beetje gekleurd kopje. Verder worden de asperges nog ingedeeld in diverse diktes. Al met al zijn er zo'n 20 verschillende sorteringen.</p><br />

