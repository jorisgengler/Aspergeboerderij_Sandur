        	<h1>Roerbak asperges en courgette</h1>

<p><i>Ingrediënten:</i></p>

<p>·        8 asperges</p>
<p>·        1 kleine courgette</p>
<p>·        150 gram magere speklapjes</p>
<p>·        1 lente-uitje</p>
<p>·        2 sjalotjes</p>
<p>·        2 teentjes knoflook</p>
<p>·        1 eetl. pijnboompitten</p>
<p>·        2 eetl. plantaardige olie</p>
<p>·        ½ theel. grof gemalen zwarte peper</p>
<p>·        2 eetl sojasaus/ketjap</p>
<p>·        zout</p><br />

<p><i>Bereiden:</i></p>
<p>Blancheer de geschilde asperges 2-3 minuten in kokend water met eventueel een beetje zout en giet ze af. Snijd de courgette in plakjes, de speklapjes in reepjes en het lente-uitje met een deel van het groen in smalle ringetjes. Snijd de sjalotjes en de knoflook fijn. Rooster de pijnboompitten in een droge koekenpan op halfhoog vuur lichtbruin, schud ze af en toe om. Verhit een grillpan en bestrijk de asperges en de courgette dun met olie. Rooster de courgette 2 minuten aan beide kanten en de asperges rondom tot ze lichtgekleurd zijn. Verhit in een wok de rest van de olie en roerbak de reepjes vlees 4 minuten. Doe de sjalot en knoflook erbij en roerbak 2 minuten mee. Voeg de peper, asperges, courgette en lente-ui toe en roerbak nog 2 minuten. Schep de sojasaus erdoor en breng op smaak met zout. Serveer het gerecht heet en bestrooi het met de pijnboompitten. Lekker met basmatirijst.</p><br />

<p>Smakelijk eten!</p>



