        	<h1>Pittige pepersoep met asperges</h1>
<p><i>Ingrediënten:</i></p>
<p>·         8 dikke witte asperges</p>
<p>·         2 rode pepertjes, gehalveerd en zaadlijsten verwijderd</p>
<p>·         50 g boter</p>
<p>·         1 ui, fijngesnipperd</p>
<p>·         2 teentjes knoflook, fijngehakt</p>
<p>·         50 g bloem</p>
<p>·         250 ml gezeefde tomaten</p>
<p>·         100 ml slagroom of crème fraiche</p>
<p>·         peper en zout </p><br />

<p><i>Bereiden:</i></p>
<p>Schil de asperges. Doe één pepertje, asperges, asperge-uiteinden en schillen (gewassen!) in een pan en zet dit net onder water met  eventueel een beetje zout. Breng dit aan de kook en haal na 6 minuten de asperges eruit. Laat de rest nog 30 minuten zachtjes trekken. Hierna zeven en het kookvocht bewaren. Hak het resterende pepertje fijn. Smelt de boter en bak de ui met de knoflook en pepertje 2-3 minuten. Roer de bloem erdoor en bak deze 1 minuut mee om te garen. Schenk de gezeefde tomaten erbij en roer tot een licht- gebonden en egale soep. Schenk er 500 ml aspergekookvocht en de slagroom of crème fraiche bij en roer tot een glad mengsel. Voeg eventueel nog zoveel aspergevocht toe tot de soep de gewenste dikte heeft bereikt. Breng de soep op smaak met peper en zout. Verdeel de stukken asperges over 4 borden of koppen en bestrooi eventueel met fijngehakte rode peper.</p><br />

<p>Smakelijk eten!     </p>
